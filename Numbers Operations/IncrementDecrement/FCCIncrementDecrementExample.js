/* Increment using i++;
    Below is the same as doing:
      increment = increment + 1;
    Which is still 99 + 1
*/

var increment = 99;
increment++; // now equal to 100

/* Decrement using i--;
   Below is the same as doing:
    decrement = decrement - 1;
   Which is still 55 - 1
*/

var decrement = 55; 
decrement--; // now equal to 54
