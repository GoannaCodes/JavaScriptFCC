//Incrment using i++;
var increment = 99;
increment++; //now equal to 100

/* Above is the same as doing:
    increment = increment + 1;
  Which is still 99 + 1
*/


//Decrement using i--;
var decrement = 55; 
decrement--; //now equal to 54

/* Above is the same as doing:
    decrement = decrement - 1;
   Which is still 55 - 1
*/
